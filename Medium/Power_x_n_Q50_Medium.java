
package Power_x_n_Q50_Medium;

/*
 * In Java, the built-in method Math.pow(x, n) is used
 * to calculate the power of a number.
 *
 * It returns x raised to the power n (x^n).
 */
class Solution {
    public double myPow(double x, int n) {

        double result = Math.pow(x,n);

        return result;

    }
}